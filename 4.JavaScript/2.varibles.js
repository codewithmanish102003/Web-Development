//variable with var
var x=5;
var y=6;
var z=x+y;
console.log(z);
console.log(typeof z);

//var can be redeclared and reassigned
var z=15;
z=16;
console.log(z);

//variable with let
let a=5;
console.log(a);
//cannot be redeclared but can be reassigned
//let a=78;
a=78;
console.log(a);

//variable with const
const b=12;
console.log(b);
//cannot be redeclared and reassigned
b=34;
console.log(b);




